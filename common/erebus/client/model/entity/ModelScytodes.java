		float cozy2 = MathHelper.cos(par1 * 1.0F) * 0.4F * par2;
		float cosxnz1 = MathHelper.cos(par1 * 0.5F + (float) Math.PI) * 0.3F * par2;
		LBL1.rotateAngleZ = cosz1 - 0.45F;
		LML1.rotateAngleZ = cosz2 - 0.45F;
		LFL1.rotateAngleZ = cosz1 - 0.45F;
		LFFL1.rotateAngleZ = cosz2 - 0.45F;
		RBL1.rotateAngleZ = -cosz2 + 0.45F;
