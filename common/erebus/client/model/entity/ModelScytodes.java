		RFFL1.rotateAngleZ = -cosz1 + 0.45F;

		LBL1.rotateAngleY = cozy1 - 0.6981317F;
		LML1.rotateAngleY = cozy2 - 0.2617994F;
		LFL1.rotateAngleY = cozy1 + 0.2617994F;
